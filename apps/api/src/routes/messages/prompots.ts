export const CHARACTER_PROMPTS: Record<BehaviorValue, string> = {
  supporter: SUPPORTER_CHARACTER,
  rival: RIVAL_CHARACTER,
  cool: COOL_CHARACTER,
  coach: COACH_CHARACTER,
  swordsman: SWORDSMAN_CHARACTER,
  trickster: TRICKSTER_CHARACTER,
};

export interface BuildPromptInput {
  type: MessageType;
  behavior: BehaviorValue;
  objective: string | null;
  purpose: string | null;
}

export const buildPrompt = ({
  type,
  behavior,
  objective,
  purpose,
}: BuildPromptInput): string => {
  const objectiveLine = objective ?? "(未設定)";
  const purposeLine = purpose ?? "(未設定)";

  return [
    "# 共通プロンプト",
    TIMING_PROMPTS[type],
    "",
    "# キャラクター",
    CHARACTER_PROMPTS[behavior],
    "",
    "# 入力値",
    `- objective: ${objectiveLine}`,
    `- purpose: ${purposeLine}`,
  ].join("\n");
};
